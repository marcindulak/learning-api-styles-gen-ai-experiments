"""Step definitions for NFR-002: encrypted and unencrypted requests.

The HTTP scenario reuses the existing ``a client sends GET to "..."`` step
in fr_009_steps.py, which already routes ``http://``-prefixed URLs through
:mod:`urllib.request` (added in ENTRY 012 for NFR-001).

The HTTPS scenario adds a parallel step that loads the server's self-signed
certificate as a trust anchor and dispatches the request through urllib
with that certificate trusted. Routing the trust-the-self-signed-cert path
through a separate step keeps the existing GET step's failure modes
unchanged for every other feature.
"""

from __future__ import annotations

import os
import socket
import ssl
import urllib.error
import urllib.request

from behave import given, then, when


class _LiveHttpResponse:
    """Adapter exposing ``status_code`` and ``content`` like Django's response.

    Duplicated from fr_009_steps.py because behave loads step files via
    ``exec_file`` rather than the import system, so a sibling import would
    fail at load time. The two attributes are the only ones the existing
    ``the response status is N`` step reads.
    """

    def __init__(self, status_code: int, content: bytes) -> None:
        self.status_code = status_code
        self.content = content


# Both NFR-002 scenarios assert ports 8000 and 8443; capturing the values
# here keeps the literal numbers next to the comment that explains them.
_HTTP_PORT = 8000
_HTTPS_PORT = 8443

# Path of the self-signed certificate generated by ``src/scripts/startup.sh``
# inside the application container. The directory follows the
# ``APP_TLS_CERTS_DIR`` env var the Dockerfile defines; falling back to the
# documented default lets a non-container test runner discover the same path.
_DEFAULT_CERT_DIR = "/etc/wfs/ssl/certs"
_CERT_FILENAME = "server.crt"

# Wall-clock budget for the local-network HTTPS request. Matches the budget
# ENTRY 012 set for the live HTTP request and FR-005's "within 5 seconds".
_REQUEST_TIMEOUT_SECONDS = 5.0

# Wall-clock budget for the precondition socket connect attempt. Short enough
# that a misconfigured port surfaces quickly; long enough that a slow CI
# scheduler still completes the connect in time.
_PRECONDITION_CONNECT_TIMEOUT_SECONDS = 2.0


def _server_cert_path() -> str:
    cert_dir = os.environ.get("APP_TLS_CERTS_DIR", _DEFAULT_CERT_DIR)
    return os.path.join(cert_dir, _CERT_FILENAME)


@given(
    "the service is running with HTTP on port {http_port:d} "
    "and HTTPS on port {https_port:d}"
)
def step_service_running_http_https(
    context, http_port: int, https_port: int
) -> None:
    # Sanity-check the dual-port setup: a TCP connect to each port confirms a
    # listener is bound. A misconfigured stack (TLS_ENABLE=0, missing port
    # mapping, forwarder crash) fails here with a clear error rather than
    # later in the When step where the failure mode is harder to read.
    assert http_port == _HTTP_PORT, (
        f"Expected HTTP port {_HTTP_PORT}, got {http_port}."
    )
    assert https_port == _HTTPS_PORT, (
        f"Expected HTTPS port {_HTTPS_PORT}, got {https_port}."
    )
    for port in (http_port, https_port):
        with socket.create_connection(
            ("localhost", port), timeout=_PRECONDITION_CONNECT_TIMEOUT_SECONDS
        ):
            pass


@when(
    'a client sends GET to "{url}" trusting the service certificate'
)
def step_client_get_trusting_cert(context, url: str) -> None:
    # Build an SSL context that trusts only the service's self-signed
    # certificate. ``check_hostname=True`` plus the cert's
    # ``subjectAltName=DNS:localhost,IP:127.0.0.1`` (set in startup.sh) means
    # the handshake also validates the URL host, so a typo in the request
    # URL surfaces as a hostname mismatch instead of silently passing.
    cert_path = _server_cert_path()
    ssl_context = ssl.create_default_context(cafile=cert_path)
    ssl_context.check_hostname = True

    context.tls_handshake_succeeded = False
    try:
        with urllib.request.urlopen(
            url, timeout=_REQUEST_TIMEOUT_SECONDS, context=ssl_context
        ) as response:
            context.response = _LiveHttpResponse(response.status, response.read())
            context.tls_handshake_succeeded = True
    except urllib.error.HTTPError as exc:
        # An HTTP error response (e.g. 4xx/5xx) is still a successful TLS
        # handshake; only the application-layer status code differs.
        context.response = _LiveHttpResponse(exc.code, exc.read())
        context.tls_handshake_succeeded = True
    if hasattr(context, "response_json"):
        del context.response_json


@then("the TLS handshake succeeds")
def step_tls_handshake_succeeds(context) -> None:
    assert getattr(context, "tls_handshake_succeeded", False), (
        "TLS handshake did not succeed; the urlopen() call raised before a "
        "response was returned."
    )
